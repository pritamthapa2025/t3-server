const fmt = (n: number, decimals = 2) =>
  `$${n.toLocaleString("en-US", { minimumFractionDigits: decimals, maximumFractionDigits: decimals })}`;

const fmtPct = (n: number) => `${n.toFixed(1)}%`;

function formatDateRange(startDate?: string, endDate?: string): string {
  const opts: Intl.DateTimeFormatOptions = { month: "short", day: "numeric", year: "numeric" };
  const from = startDate ? new Date(startDate).toLocaleDateString("en-US", opts) : "All time";
  const to = endDate ? new Date(endDate).toLocaleDateString("en-US", opts) : "Today";
  return `${from} – ${to}`;
}

function generateCsvFromRows(columns: string[], rows: string[][]): string {
  const escape = (s: string) => (s.includes(",") || s.includes('"') ? `"${s.replace(/"/g, '""')}"` : s);
  const header = columns.map(escape).join(",");
  const body = rows.map((r) => r.map(escape).join(",")).join("\n");
  return `${header}\n${body}`;
}

function streamPdf(res: Response, buffer: Buffer, filename: string) {
  res.setHeader("Content-Type", "application/pdf");
  res.setHeader("Content-Disposition", `attachment; filename="${filename}"`);
  res.setHeader("Content-Length", buffer.length);
  res.end(buffer);
}

function streamCsv(res: Response, csv: string, filename: string) {
  res.setHeader("Content-Type", "text/csv");
  res.setHeader("Content-Disposition", `attachment; filename="${filename}"`);
  res.end(csv);
}

