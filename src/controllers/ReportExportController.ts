async function buildMonthlyRevenueTrendReport(
  _orgId: string | undefined,
  filters: { startDate?: string; endDate?: string }
) {
  // getMonthlyRevenueTrend does not filter by org; pass date filters
  const data = await getMonthlyRevenueTrend(filters);

  const totalRevenue = data.reduce((s, m) => s + m.revenue, 0);
  const totalCost = data.reduce((s, m) => s + m.cost, 0);
  const totalProfit = data.reduce((s, m) => s + m.profit, 0);

  const summaryCardsHtml = buildSummaryCardsHtml([
    { label: "Months", value: String(data.length) },
    { label: "Total Revenue", value: fmt(totalRevenue), variant: "positive" },
    { label: "Total Cost", value: fmt(totalCost) },
    { label: "Total Profit", value: fmt(totalProfit), variant: totalProfit >= 0 ? "positive" : "negative" },
  ]);

  const hasTargets = data.some((m) => m.target != null);
  const columns: Array<{ label: string; align?: "left" | "right" | "center" }> = [
    { label: "Month" },
    { label: "Revenue", align: "right" },
    { label: "Cost", align: "right" },
    { label: "Profit", align: "right" },
    ...(hasTargets ? [{ label: "Target", align: "right" as const }] : []),
    ...(hasTargets ? [{ label: "vs Target", align: "right" as const }] : []),
  ];

  const tableRows = data.map((m) => {
    const row: any[] = [
      m.month,
      { text: fmt(m.revenue), align: "right" },
      { text: fmt(m.cost), align: "right" },
      { text: fmt(m.profit), align: "right" },
    ];
    if (hasTargets) {
      const target = m.target ?? 0;
      const diff = m.revenue - target;
      row.push({ text: target > 0 ? fmt(target) : "—", align: "right" });
      row.push({ text: target > 0 ? `${diff >= 0 ? "+" : ""}${fmt(diff)}` : "—", align: "right" });
    }
    return row;
  });

  const totalsRow: string[] = ["TOTAL", fmt(totalRevenue), fmt(totalCost), fmt(totalProfit)];
  if (hasTargets) totalsRow.push("—", "—");

  const mainContentHtml = buildDataTableHtml({
    sectionTitle: "Monthly Revenue vs Cost vs Profit",
    columns,
    rows: tableRows,
    totalsRow,
  });

  const csvColumns = ["Month", "Revenue", "Cost", "Profit", ...(hasTargets ? ["Target", "vs Target"] : [])];
  const csvRows = data.map((m) => {
    const row = [m.month, fmt(m.revenue), fmt(m.cost), fmt(m.profit)];
    if (hasTargets) {
      const target = m.target ?? 0;
      const diff = m.revenue - target;
      row.push(target > 0 ? fmt(target) : "—", target > 0 ? `${diff >= 0 ? "+" : ""}${fmt(diff)}` : "—");
    }
    return row;
  });

  return { summaryCardsHtml, mainContentHtml, csvColumns, csvRows };
}

