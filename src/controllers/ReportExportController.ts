// ──────────────────────────────────────────────────────────
// Main export handler
// GET /org/reports/financial/:reportType/export
// ──────────────────────────────────────────────────────────

export const exportFinancialReportHandler = async (req: Request, res: Response) => {
  try {
    const reportType = req.params.reportType as string;
    const format = (req.query.format as string) || "pdf";
    const organizationId = req.query.organizationId as string | undefined;
    const startDate = req.query.startDate as string | undefined;
    const endDate = req.query.endDate as string | undefined;
    const filters: { startDate?: string; endDate?: string } = {
      ...(startDate !== undefined && { startDate }),
      ...(endDate !== undefined && { endDate }),
    };

    const meta = REPORT_META[reportType];
    if (!meta) {
      return res.status(404).json({ success: false, error: `Unknown report type: ${reportType}` });
    }

    // Build report data
    let report: { summaryCardsHtml: string; mainContentHtml: string; csvColumns: string[]; csvRows: string[][] };

    switch (reportType) {
      case "profit-loss":
        report = await buildProfitLossReport(organizationId, filters);
        break;
      case "job-profitability":
        report = await buildJobProfitabilityReport(organizationId, filters);
        break;
      case "expense-by-category":
        report = await buildExpenseByCategoryReport(organizationId, filters);
        break;
      case "revenue-by-client":
        report = await buildRevenueByClientReport(organizationId, filters);
        break;
      case "invoice-aging":
        report = await buildInvoiceAgingReport(organizationId, filters);
        break;
      case "cash-flow":
        report = await buildCashFlowReport(organizationId, filters);
        break;
      case "vendor-spend":
        report = await buildVendorSpendReport(organizationId, filters);
        break;
      case "monthly-revenue-trend":
        report = await buildMonthlyRevenueTrendReport(organizationId, filters);
        break;
      case "labor-cost":
        report = await buildLaborCostReport(organizationId, filters);
        break;
      case "client-outstanding":
        report = await buildClientOutstandingReport(organizationId, filters);
        break;
      case "technician-profit":
        report = await buildTechnicianProfitReport(organizationId, filters);
        break;
      case "payment-collection":
        report = await buildPaymentCollectionReport(organizationId, filters);
        break;
      case "client-spend":
        report = await buildClientSpendReport(organizationId, filters);
        break;
      case "job-cost-breakdown":
        report = await buildJobCostBreakdownReport(organizationId, filters);
        break;
      case "invoice-summary":
        report = await buildInvoiceSummaryReport(organizationId, filters);
        break;
      case "monthly-expense-trend":
        report = await buildMonthlyExpenseTrendReport(organizationId, filters);
        break;
      case "inventory-valuation":
        report = await buildInventoryValuationReport(organizationId, filters);
        break;
      default:
        return res.status(404).json({ success: false, error: `Unknown report type: ${reportType}` });
    }

    const dateRange = formatDateRange(startDate, endDate);
    const generatedDate = new Date().toLocaleDateString("en-US", { month: "long", day: "numeric", year: "numeric" });
    const safeReportId = reportType.toUpperCase().replace(/-/g, "_");

    if (format === "csv") {
      const csv = generateCsvFromRows(report.csvColumns, report.csvRows);
      const filename = `t3-${reportType}-${new Date().toISOString().slice(0, 10)}.csv`;
      return streamCsv(res, csv, filename);
    }

    // Default: PDF
    const pdfBuffer = await generateFinancialReportPDF({
      reportTitle: meta.title,
      reportSubtitle: meta.subtitle,
      reportId: safeReportId,
      dateRange,
      organizationName: "T3 Mechanical Inc.",
      generatedDate,
      summaryCardsHtml: report.summaryCardsHtml,
      mainContentHtml: report.mainContentHtml,
    });

    const filename = `t3-${reportType}-${new Date().toISOString().slice(0, 10)}.pdf`;
    return streamPdf(res, pdfBuffer, filename);
  } catch (err) {
    console.error("[ReportExportController]", err);
    return res.status(500).json({
      success: false,
      error: err instanceof Error ? err.message : "Failed to generate report",
    });
  }
};
