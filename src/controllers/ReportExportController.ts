async function buildMonthlyExpenseTrendReport(
  orgId: string | undefined,
  filters: { startDate?: string; endDate?: string }
) {
  // Returns Array<{ month, [categoryKey]: number, ... }>
  const raw = await getMonthlyExpenseTrend(orgId, filters);

  // Collect all category keys across all months
  const categoryKeys = Array.from(
    new Set(raw.flatMap((m) => Object.keys(m).filter((k) => k !== "month")))
  );

  const categoryLabels: Record<string, string> = {
    materials: "Materials", equipment: "Equipment", transportation: "Transportation",
    permits: "Permits", subcontractor: "Subcontractor", utilities: "Utilities",
    tools: "Tools", safety: "Safety", fleet: "Fleet", maintenance: "Maintenance",
    fuel: "Fuel", tires: "Tires", registration: "Registration", repairs: "Repairs",
    insurance: "Insurance", office_supplies: "Office Supplies", rent: "Rent",
    internet: "Internet", job_labor: "Job Labor", other: "Other",
  };

  // Grand totals per category
  const categoryTotals: Record<string, number> = {};
  raw.forEach((m) => {
    categoryKeys.forEach((k) => {
      categoryTotals[k] = (categoryTotals[k] ?? 0) + (m[k] ?? 0);
    });
  });

  const grandTotal = Object.values(categoryTotals).reduce((s, v) => s + v, 0);
  const monthlyTotals = raw.map((m) =>
    categoryKeys.reduce((s, k) => s + (m[k] ?? 0), 0)
  );
  const totalAllMonths = monthlyTotals.reduce((s, v) => s + v, 0);

  const summaryCardsHtml = buildSummaryCardsHtml([
    { label: "Months", value: String(raw.length) },
    { label: "Total Expenses", value: fmt(totalAllMonths) },
    { label: "Categories", value: String(categoryKeys.length) },
    {
      label: "Avg Monthly",
      value: raw.length > 0 ? fmt(totalAllMonths / raw.length) : "—",
    },
  ]);

  // Table: Month | Total | Top-3 categories
  const topCats = categoryKeys
    .slice()
    .sort((a, b) => (categoryTotals[b] ?? 0) - (categoryTotals[a] ?? 0))
    .slice(0, 3);

  const columns = [
    { label: "Month" },
    ...topCats.map((k) => ({ label: categoryLabels[k] ?? k, align: "right" as const })),
    { label: "Other", align: "right" as const },
    { label: "Month Total", align: "right" as const },
  ];

  const tableRows = raw.map((m, i) => {
    const topVals = topCats.map((k) => ({ text: fmt(m[k] ?? 0), align: "right" }));
    const otherVal = categoryKeys
      .filter((k) => !topCats.includes(k))
      .reduce((s, k) => s + (m[k] ?? 0), 0);
    return [m.month, ...topVals, { text: fmt(otherVal), align: "right" }, { text: fmt(monthlyTotals[i] ?? 0), align: "right" }] as any[];
  });

  const topTotals = topCats.map((k) => fmt(categoryTotals[k] ?? 0));
  const otherTotal = categoryKeys.filter((k) => !topCats.includes(k)).reduce((s, k) => s + (categoryTotals[k] ?? 0), 0);
  const totalsRow = ["TOTAL", ...topTotals, fmt(otherTotal), fmt(grandTotal)];

  const mainContentHtml = buildDataTableHtml({
    sectionTitle: "Monthly Expense Trend by Category",
    columns,
    rows: tableRows,
    totalsRow,
  });

  const csvColumns = ["Month", ...categoryKeys.map((k) => categoryLabels[k] ?? k), "Month Total"];
  const csvRows = raw.map((m, i) => [
    m.month,
    ...categoryKeys.map((k) => fmt(m[k] ?? 0)),
    fmt(monthlyTotals[i] ?? 0),
  ]);

  return { summaryCardsHtml, mainContentHtml, csvColumns, csvRows };
}

